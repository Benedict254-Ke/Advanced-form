public class BitwiseAndExample {
    public static void main(String[] args) {
        int num1 = 2;  // Binary: 10
        int num2 = 3;  // Binary: 11

        // Perform bitwise AND operation
        int result = num1 & num2;  // Binary result: 10

        // Print the result
        System.out.println("The result of bitwise AND operation between 2 and 3 is: " + result);
    }
}